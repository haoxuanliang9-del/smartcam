OledDisplay::~OledDisplay() { stop(); }

bool OledDisplay::init(const DisplayConfig& config) {
    config_ = config;
    gpio_dc_ = config.gpio_dc;
    gpio_rst_ = config.gpio_rst;
    gpio_cs_ = config.gpio_cs;

    SpiConfig spi_cfg;
    spi_cfg.device = config.spi_device;
    if (!spi_.open(spi_cfg)) {
        SPDLOG_ERROR("Failed to open SPI for OLED display");
        return false;
    }

    for (int pin : {gpio_cs_, gpio_dc_, gpio_rst_}) {
        if (!SpiHal::gpio_export(pin)) {
            SPDLOG_WARN("Failed to export GPIO {}", pin);
        }
        std::this_thread::sleep_for(std::chrono::milliseconds(10));
        if (!SpiHal::gpio_set_direction(pin, "out")) {
            SPDLOG_WARN("Failed to set direction for GPIO {}", pin);
        }
    }
    SpiHal::gpio_write(gpio_cs_, 1);

    reset_hardware();
    init_ss1306();

    clear_buffer();
    draw_text(0, 0, "Initializing...");
    render();

    SPDLOG_INFO("OLED Display initialized (spi={})", config.spi_device);
    return true;
}

void OledDisplay::start() {
    if (running_) return;
    running_ = true;
    std::thread(&OledDisplay::display_loop, this).detach();
    SPDLOG_INFO("OledDisplay started");
}

void OledDisplay::stop() {
    if (!running_) return;
    running_ = false;
    std::this_thread::sleep_for(std::chrono::milliseconds(200));
    clear_buffer();
    render();
    for (int pin : {gpio_cs_, gpio_dc_, gpio_rst_}) {
        SpiHal::gpio_unexport(pin);
    }
    spi_.close();
    SPDLOG_INFO("OledDisplay stopped");
}

void OledDisplay::update_bitrate(uint32_t kbps) {
    current_bitrate_.store(kbps);
}

void OledDisplay::update_sensor(float temp_c, float humidity_pct) {
    current_temp_.store(temp_c);
    current_humidity_.store(humidity_pct);
    last_sensor_update_.store(
        std::chrono::duration_cast<std::chrono::milliseconds>(
            std::chrono::steady_clock::now().time_since_epoch()).count());
}

void OledDisplay::display_loop() {
    while (running_) {
        render();
        std::this_thread::sleep_for(std::chrono::seconds(1));
    }
}

void OledDisplay::render() {
    clear_buffer();

    auto now_ms = std::chrono::duration_cast<std::chrono::milliseconds>(
        std::chrono::steady_clock::now().time_since_epoch()).count();
    bool sensor_stale = (now_ms - last_sensor_update_.load()) > 5000;

    std::string wifi = get_wifi_status();
    draw_text(0, 0, wifi);

    std::ostringstream sensor_line;
    if (sensor_stale) {
        sensor_line << "T: --.-C  H: --.-%";
    } else {
        float t = current_temp_.load();
        float h = current_humidity_.load();
        sensor_line << "T: " << std::fixed << std::setprecision(1) << t
                   << "C  H: " << std::setprecision(1) << h << "%";
    }
    draw_text(3, 0, sensor_line.str());

    std::ostringstream rate_line;
    rate_line << "Rate: " << current_bitrate_.load() << " kbps";
    draw_text(6, 0, rate_line.str());

    write_data(buffer_, sizeof(buffer_));
}

void OledDisplay::clear_buffer() {
    std::memset(buffer_, 0, sizeof(buffer_));
}

void OledDisplay::draw_text(int row, int col, const std::string& text) {
    if (row >= ROWS || col >= COLS) return;
    for (size_t i = 0; i < text.size() && (col + i) < COLS; ++i) {
        char c = text[i];
        if (c < 0x20 || c > 0x7E) c = ' ';
        int font_idx = c - 0x20;
        for (int x = 0; x < FONT_WIDTH; ++x) {
            uint8_t col_data = FONT_6X8[font_idx][x];
            int buf_col = (col + i) * FONT_WIDTH + x;
            if (buf_col < SCREEN_WIDTH) {
                buffer_[row * SCREEN_WIDTH + buf_col] |= col_data;
            }
        }
    }
}

void OledDisplay::write_command(uint8_t cmd) {
    SpiHal::gpio_write(gpio_cs_, 0);
    SpiHal::gpio_write(gpio_dc_, 0);
    spi_.transfer(&cmd, nullptr, 1);
    SpiHal::gpio_write(gpio_cs_, 1);
}

void OledDisplay::write_data(const uint8_t* data, size_t len) {
    SpiHal::gpio_write(gpio_cs_, 0);
    SpiHal::gpio_write(gpio_dc_, 1);
    spi_.transfer(data, nullptr, len);
    SpiHal::gpio_write(gpio_cs_, 1);
}

void OledDisplay::reset_hardware() {
    SpiHal::gpio_write(gpio_rst_, 0);
    std::this_thread::sleep_for(std::chrono::milliseconds(10));
    SpiHal::gpio_write(gpio_rst_, 1);
    std::this_thread::sleep_for(std::chrono::milliseconds(10));
}

void OledDisplay::init_ss1306() {
    write_command(0xAE);
    write_command(0xD5); write_command(0x80);
    write_command(0xA8); write_command(0x3F);
    write_command(0xD3); write_command(0x00);
    write_command(0x40);
    write_command(0x8D); write_command(0x14);
    write_command(0x20); write_command(0x00);
    write_command(0xA1);
    write_command(0xC8);
    write_command(0xDA); write_command(0x12);
    write_command(0x81); write_command(0xFF);
    write_command(0xD9); write_command(0xF1);
    write_command(0xDB); write_command(0x40);
    write_command(0xA6);
    write_command(0xAF);
}

std::string OledDisplay::get_wifi_status() {
    const char* interfaces[] = {"wlan0", "wlan1", "wlp1s0", nullptr};
    for (int i = 0; interfaces[i]; ++i) {
        std::ifstream carrier("/sys/class/net/" + std::string(interfaces[i]) + "/carrier");
        if (!carrier.is_open()) continue;
        int linked = 0;
        carrier >> linked;
        if (linked) {
            std::string cmd = "ip -4 addr show " + std::string(interfaces[i]) +
                               " | grep 'inet ' | awk '{print $2}' | cut -d/ -f1";
            FILE* pipe = popen(cmd.c_str(), "r");
            if (pipe) {
                char buf[32] = {};
                if (fgets(buf, sizeof(buf), pipe)) {
                    buf[strcspn(buf, "\n")] = '\0';
                    pclose(pipe);
                    std::string ip(buf);
                    if (ip.length() > 16) ip = ip.substr(0, 16);
                    return "WiFi: " + ip + " [ON]";
                }
                pclose(pipe);
            }
            return "WiFi: Connected [ON]";
        }
    }
    return "WiFi: [OFF]";
}

